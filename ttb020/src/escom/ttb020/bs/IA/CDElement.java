package escom.ttb020.bs.IA;

public class CDElement {
	private String name;
	private String intent;
	private ElementCD type;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public ElementCD getType() {
		return type;
	}
	public void setType(ElementCD type) {
		this.type = type;
	}
	public String getIntent() {
		return intent;
	}
	public void setIntent(String intent) {
		this.intent = intent;
	}
}
